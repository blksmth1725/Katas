function litres(time) {
  const name = "Chris";
  const drank = Math.floor(time * 0.5);
  return `${name} will go on a ${time} hour hike and will probably end up drinking 
  ${drank} litres of water`;
}

console.log(litres(5));
console.log(litres(7));
console.log(litres(6));
console.log(litres(15));
console.log(litres(4));
console.log(litres(19));
